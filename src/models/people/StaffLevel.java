package models.people;

/**
 * Enum that states the possible levels a LibraryStaff object can have.
 * <p>
 * ADMINISTRATOR: provides full access to all system functionalities and operations.
 * MANAGER: provides partial access to system functionalities and operations.
 * ATTENDANT: provides limited access to system functionalities and operations.
 *
 * @author Matheus Reato (RA: 244088), Caio Taishi (RA: 242908)
 */

public enum StaffLevel {
    ADMINISTRATOR, MANAGER, ATTENDANT
}
